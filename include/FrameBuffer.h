#ifndef FRAMEBUFFER_H
#define FRAMEBUFFER_H

#ifdef DARWIN
    #include <GLFW/glfw3.h>
    #include <OpenGL/gl3.h>
#else
    #include <GL/glew.h>
    #include <GL/gl.h>
#endif

#include "AbstractOpenGLObject.h"

class FrameBuffer : public AbstractOpenGlObject
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief default constructor to create our frame buffer
    //----------------------------------------------------------------------------------------------------------------------
    FrameBuffer();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief default destructor removes frambuffers
    //----------------------------------------------------------------------------------------------------------------------
    ~FrameBuffer();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief bind our frame buffer
    //----------------------------------------------------------------------------------------------------------------------
    void bind();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief unbind our frame buffer
    //----------------------------------------------------------------------------------------------------------------------
    void unbind();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set our frame buffer texture
    //----------------------------------------------------------------------------------------------------------------------
    void setFrameBufferTexture(GLenum _attachment,GLuint _texture,GLint _level);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief set the draw buffers of our frame buffer
    //----------------------------------------------------------------------------------------------------------------------
    void setDrawbuffers(GLsizei _n,const GLenum *_bufs);
    //----------------------------------------------------------------------------------------------------------------------
};

#endif // FRAMEBUFFER_H
