#ifndef ABSTRACTOPENGLOBJECT_H
#define ABSTRACTOPENGLOBJECT_H

#ifdef DARWIN
    #include <GLFW/glfw3.h>
    #include <OpenGL/gl3.h>
#else
    #include <GL/glew.h>
    #include <GL/gl.h>
#endif

class AbstractOpenGlObject
{
public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief default constructor, does nothing
    //----------------------------------------------------------------------------------------------------------------------
    AbstractOpenGlObject(){}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief defalut destructor
    //----------------------------------------------------------------------------------------------------------------------
    virtual ~AbstractOpenGlObject(){}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief virtual method to bind our openGL object
    //----------------------------------------------------------------------------------------------------------------------
    virtual void bind(){}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief virtual method to unbind our openGL object
    //----------------------------------------------------------------------------------------------------------------------
    virtual void unbind(){}
    //----------------------------------------------------------------------------------------------------------------------
protected:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief handle to our opengGL object
    //----------------------------------------------------------------------------------------------------------------------
    GLenum m_handle;
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief render target type
    //----------------------------------------------------------------------------------------------------------------------
    GLenum m_target;
    //----------------------------------------------------------------------------------------------------------------------
};

#endif // ABSTRACTOPENGLOBJECT_H
